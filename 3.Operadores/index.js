//TIPOS DE OPERDPORES
//1.OPERADORES DE ASIGNACIÓN
  /*BRENY - el operdore de asignacion (=) asigna un valor a su operando izquierdo basandose en el valor de su
    operando derecho.*/
  /*LUZ - Los operadores de asignación devuelven el valor del objeto especificado
    por el operando izquierdo después de la asignación(=)*/
//2.OPERADORES DE COMPARCION
  /*BRENY - Ese operador comprueba si sus dos operandos, son iguales y le da un resultado booleano.
  /*LUZ - Se encarga de coparar dos expresiones y devuelve un valor booleano(True, False o Null.) 
    que representa la relacion de sus valores.*/
//3.OPERADORES DE LOGICA
  /*BRENY - Son operadores para trabajar con datos booleanos y logicos*/
  /*LUZ - Se usan para combinar dos valores Booleanos y devolver un resultado verdadero, falso o nulo*/
//4.OPERADORES ARITMÉTICOS
  /*BRENY - Con ellos se pueden realizar operaciones y calculos matematicos.*/
  /*LUZ - Se usan para calcular un valor de dos a más o cambiar 
  su signo de un numero de positivo a negativo o vicibersa*/
//--------------------------------------------------------------------------------------------------------//
